    //Função para destar célula da tabela
    function destacarCelula(idCelula) {
    let celula = document.getElementById(idCelula);

    if (celula) {
        celula.classList.add("destaque");

        // remove a classe após a animação para poder reaplicar depois
        setTimeout(() => {
            celula.classList.remove("destaque");
        }, 2000); // mesmo tempo da animação
    }
}



