
    function lerMais1(){
      var mostrar1 = document.getElementById("mostrar_mais1");
      var maisTexto1 = document.getElementById("maiss1");

      if(mostrar1.style.display === "none"){
        mostrar1.style.display = "inline";
        maisTexto1.style.display = "none";
      } else {
        mostrar1.style.display = "none";
        maisTexto1.style.display = "inline";
     }
    }
